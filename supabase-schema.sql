create or replace function private.write_audit_log()
returns trigger language plpgsql security definer
set search_path = public, pg_temp
as $$
declare
  v_role public.app_role;
  v_student_id bigint;
  v_record_id text;
  v_headers jsonb;
  v_ip inet;
  v_request_id text;
begin
  select role into v_role from public.profiles where id = (select auth.uid());
  begin
    v_headers := nullif(current_setting('request.headers', true), '')::jsonb;
    v_ip := split_part(coalesce(v_headers->>'x-forwarded-for', v_headers->>'cf-connecting-ip'), ',', 1)::inet;
    v_request_id := coalesce(v_headers->>'x-request-id', v_headers->>'cf-ray');
  exception when others then
    v_ip := null;
    v_request_id := null;
  end;
  v_student_id := case when tg_table_name = 'students' then coalesce(new.id, old.id) else coalesce(new.student_id, old.student_id) end;
  v_record_id := coalesce(new.id::text, old.id::text);
  insert into public.audit_logs(actor_id, actor_role, action, table_name, record_id, student_id, old_data, new_data, ip_address, request_id)
  values (
    (select auth.uid()), v_role, tg_op, tg_table_name, v_record_id, v_student_id,
    case when tg_op in ('UPDATE', 'DELETE') then to_jsonb(old) end,
    case when tg_op in ('INSERT', 'UPDATE') then to_jsonb(new) end,
    v_ip, v_request_id
  );
  return coalesce(new, old);
end;
$$;
revoke all on function private.write_audit_log() from public, anon, authenticated;

drop trigger if exists audit_students_changes on public.students;
create trigger audit_students_changes after insert or update or delete on public.students
for each row execute function private.write_audit_log();
