create or replace function public.teacher_dashboard_counts(
  p_classroom text default null, p_date date default null, p_search text default null
) returns jsonb language plpgsql stable security invoker
set search_path = public, pg_temp
as $$
declare result jsonb;
begin
  if not (select private.is_staff()) then raise exception 'forbidden'; end if;
  select jsonb_build_object(
    'today_absences', count(*) filter (where ar.absence_date = current_date),
    'today_makeups', count(*) filter (where ar.makeup_date = current_date and ar.status = 'reserved'),
    'pending_makeups', count(*) filter (where ar.status = 'pending')
  ) into result
  from public.absence_records ar join public.students s on s.id = ar.student_id
  where (p_classroom is null or s.classroom = p_classroom)
    and (p_date is null or ar.absence_date = p_date or ar.makeup_date = p_date)
    and (p_search is null or s.name ilike '%' || p_search || '%');
  return result;
end;
$$;
revoke all on function public.teacher_dashboard_counts(text, date, text) from public, anon;
grant execute on function public.teacher_dashboard_counts(text, date, text) to authenticated;

