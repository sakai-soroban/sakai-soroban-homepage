-- さかいそろばん教室 セキュリティ優先スキーマ
-- Supabase SQL Editorで一括実行してください。
create extension if not exists pgcrypto;
create schema if not exists private;
revoke all on schema private from public, anon;
grant usage on schema private to authenticated;

do $$ begin
  create type public.app_role as enum ('guardian', 'teacher', 'admin');
exception when duplicate_object then null;
end $$;

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  role public.app_role not null default 'guardian',
  display_name text not null,
  created_at timestamptz not null default now()
);

-- 保護者1人に複数生徒を紐付ける1対多。
create table if not exists public.students (
  id bigint generated by default as identity primary key,
  guardian_id uuid not null references public.profiles(id) on delete restrict,
  name text not null,
  classroom text not null check (classroom in ('福沼教室', '穂波教室')),
  grade text not null,
  regular text not null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

-- LINEユーザーIDはサーバー側HMAC値だけ保存し、平文は保存しない。
create table if not exists private.guardian_line_identities (
  guardian_id uuid primary key references public.profiles(id) on delete cascade,
  line_user_id_hmac text not null unique,
  linked_at timestamptz not null default now(),
  last_authenticated_at timestamptz
);

-- 初回本人確認用。コード本体は保存せずハッシュだけ保存する。
create table if not exists private.guardian_link_codes (
  id uuid primary key default gen_random_uuid(),
  guardian_id uuid not null references public.profiles(id) on delete cascade,
  code_hash text not null unique,
  expires_at timestamptz not null,
  used_at timestamptz,
  created_at timestamptz not null default now()
);

create table if not exists public.absence_records (
  id uuid primary key default gen_random_uuid(),
  student_id bigint not null references public.students(id) on delete restrict,
  absence_date date not null,
  reason text not null check (char_length(reason) between 1 and 200),
  wants_makeup boolean not null default false,
  makeup_date date,
  makeup_classroom text check (makeup_classroom is null or makeup_classroom in ('福沼教室', '穂波教室')),
  makeup_slot text check (makeup_slot is null or makeup_slot in ('16:00', '17:00', '18:00')),
  status text not null default 'none' check (status in ('none', 'pending', 'reserved', 'completed')),
  created_by uuid not null default auth.uid() references auth.users(id),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.audit_logs (
  id bigint generated always as identity primary key,
  actor_id uuid,
  actor_role public.app_role,
  action text not null check (action in ('INSERT', 'UPDATE', 'DELETE')),
  table_name text not null,
  record_id text not null,
  student_id bigint,
  old_data jsonb,
  new_data jsonb,
  ip_address inet,
  request_id text,
  changed_at timestamptz not null default now()
);

create index if not exists students_guardian_id_idx on public.students(guardian_id);
create index if not exists students_name_idx on public.students(name);
create index if not exists absence_records_student_id_idx on public.absence_records(student_id);
create index if not exists absence_records_absence_date_idx on public.absence_records(absence_date);
create index if not exists absence_records_makeup_date_idx on public.absence_records(makeup_date);
create index if not exists audit_logs_changed_at_idx on public.audit_logs(changed_at desc);
create index if not exists audit_logs_student_id_idx on public.audit_logs(student_id);
create index if not exists guardian_line_identity_hmac_idx on private.guardian_line_identities(line_user_id_hmac);
create index if not exists guardian_link_codes_expiry_idx on private.guardian_link_codes(expires_at);

create or replace function private.current_user_role()
returns public.app_role language sql stable security definer
set search_path = public, pg_temp
as $$ select role from public.profiles where id = (select auth.uid()) $$;
revoke all on function private.current_user_role() from public, anon;
grant execute on function private.current_user_role() to authenticated;

create or replace function private.is_staff()
returns boolean language sql stable security definer
set search_path = public, pg_temp
as $$ select coalesce(private.current_user_role() in ('teacher', 'admin'), false) $$;
revoke all on function private.is_staff() from public, anon;
grant execute on function private.is_staff() to authenticated;

create or replace function private.is_admin()
returns boolean language sql stable security definer
set search_path = public, pg_temp
as $$ select coalesce(private.current_user_role() = 'admin', false) $$;
revoke all on function private.is_admin() from public, anon;
grant execute on function private.is_admin() to authenticated;

alter table public.profiles enable row level security;
alter table public.students enable row level security;
alter table public.absence_records enable row level security;
alter table public.audit_logs enable row level security;

revoke all on public.profiles, public.students, public.absence_records, public.audit_logs from anon;
revoke all on public.profiles, public.students, public.absence_records, public.audit_logs from authenticated;
grant select on public.profiles, public.students, public.audit_logs to authenticated;
grant select, insert, update, delete on public.absence_records to authenticated;
grant insert, update, delete on public.students to authenticated;
grant usage, select on all sequences in schema public to authenticated;

drop policy if exists profiles_select_own_or_staff on public.profiles;
create policy profiles_select_own_or_staff on public.profiles for select to authenticated
using ((select auth.uid()) is not null and (id = (select auth.uid()) or (select private.is_admin())));

drop policy if exists students_select_own_or_staff on public.students;
create policy students_select_own_or_staff on public.students for select to authenticated
using ((select auth.uid()) is not null and (guardian_id = (select auth.uid()) or (select private.is_staff())));

drop policy if exists students_admin_insert on public.students;
create policy students_admin_insert on public.students for insert to authenticated
with check ((select private.is_admin()));
drop policy if exists students_admin_update on public.students;
create policy students_admin_update on public.students for update to authenticated
using ((select private.is_admin())) with check ((select private.is_admin()));
drop policy if exists students_admin_delete on public.students;
create policy students_admin_delete on public.students for delete to authenticated
using ((select private.is_admin()));

drop policy if exists absence_guardian_insert_own_child on public.absence_records;
create policy absence_guardian_insert_own_child on public.absence_records for insert to authenticated
with check (
  (select private.current_user_role()) = 'guardian'
  and created_by = (select auth.uid())
  and exists (
    select 1 from public.students s
    where s.id = absence_records.student_id and s.guardian_id = (select auth.uid())
  )
  and status in ('none', 'reserved')
  and (
    (wants_makeup = false and status = 'none' and makeup_date is null and makeup_classroom is null and makeup_slot is null)
    or
    (wants_makeup = true and status = 'reserved' and makeup_date is not null and makeup_classroom is not null and makeup_slot is not null)
  )
);

-- 保護者には過去連絡一覧も返さず、先生・管理者のみ閲覧。
