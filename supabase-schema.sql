create or replace function private.protect_absence_ownership()
returns trigger language plpgsql
set search_path = public, pg_temp
as $$
begin
  if new.student_id is distinct from old.student_id
     or new.created_by is distinct from old.created_by
     or new.created_at is distinct from old.created_at then
    raise exception 'ownership fields are immutable';
  end if;
  return new;
end;
$$;
revoke all on function private.protect_absence_ownership() from public, anon, authenticated;

drop trigger if exists absence_protect_ownership on public.absence_records;
create trigger absence_protect_ownership
before update on public.absence_records
for each row execute function private.protect_absence_ownership();

drop policy if exists absence_admin_delete on public.absence_records;
create policy absence_admin_delete on public.absence_records for delete to authenticated
using ((select private.is_admin()));

drop policy if exists audit_staff_select on public.audit_logs;
create policy audit_staff_select on public.audit_logs for select to authenticated
